public class p10square {
    static int square(int x)
    {
        int sq=x*x;
        return sq;
    }
    public static void main(String[] args) {
        System.out.println("result is : "+square(1));
        System.out.println("result is : "+square(2));
        System.out.println("result is : "+square(3));
        System.out.println("result is : "+square(4));
        System.out.println("result is : "+square(5));
    }
}


