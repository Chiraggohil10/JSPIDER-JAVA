public class p64 {
    public static void main(String[] args) {
        String str = "tiger";
        char[] ch = str.toCharArray();
        for (int i = 0; i < ch.length; i++) {
            System.out.println(ch[i]);
        }
    }
}

// toCharArray()
// toCharArray() method helps the programmer to represent a string value in the form of a character array.
// toCharArray() method does not convert string to character array if creates an equivalent character array.
